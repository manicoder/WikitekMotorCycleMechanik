        #region Commands
        public ICommand ShowUserViewCommand => new Command(async (obj) =>
        {

            Device.BeginInvokeOnMainThread(async () =>
            {
                using (UserDialogs.Instance.Loading("Loading...", null, null, true, MaskType.Black))
                {
                    await Task.Delay(100);
                    try
                    {
                        show_user_view = true;
                        user_list = new ObservableCollection<MibudUser>();
                        GetUserList();
                    }
                    catch (Exception ex)
                    {
                    }
                }
            });

        });

        public ICommand ShowVehicleViewCommand => new Command(async (obj) =>
        {

            Device.BeginInvokeOnMainThread(async () =>
            {
                using (UserDialogs.Instance.Loading("Loading...", null, null, true, MaskType.Black))
                {
                    await Task.Delay(100);
                    try
                    {
                        if (selected_user == null)
                        {
                            await page.DisplayAlert("", "Firstly select a user.", "Ok");
                            return;
                        }

                        show_vehicle_view = true;
                        vehicle_list = new ObservableCollection<VehicleListResult>();

                        GetVehicleList();
                    }
                    catch (Exception ex)
                    {
                    }
                }
            });

        });

        public ICommand ShowServiceViewCommand => new Command(async (obj) =>
        {

            Device.BeginInvokeOnMainThread(async () =>
            {
                using (UserDialogs.Instance.Loading("Loading...", null, null, true, MaskType.Black))
                {
                    await Task.Delay(100);
                    try
                    {
                        if (selected_vehicle == null)
                        {
                            await page.DisplayAlert("", "Firstly select a vehicle.", "Ok");
                            return;
                        }

                        show_service_view = true;
                    }
                    catch (Exception ex)
                    {
                    }
                }
            });
        });


        public ICommand HideUserViewCommand => new Command(async (obj) =>
        {
            show_service_view = show_vehicle_view = show_user_view = false;
        });

        public ICommand SubmitCommand => new Command(async (obj) =>
        {
            if (selected_user == null)
            {
                await page.DisplayAlert("", "Please select a user.", "Ok");
                return;
            }
            else if (selected_vehicle == null)
            {
                await page.DisplayAlert("", "Please select a vehicle.", "Ok");
                return;
            }
            else if (selected_service == null)
            {
                await page.DisplayAlert("", "Please select a service.", "Ok");
                return;
            }
            else if (string.IsNullOrWhiteSpace(jobcard_detail.job_card_name))
            {
                await page.DisplayAlert("", "Please enter jobcard number.", "Ok");
                return;
            }

            jobcard_detail.service_type = selected_service;
            jobcard_detail.registration_no = selected_vehicle.registration_id;
            var result = await apiServices.CreateJobcard(Xamarin.Essentials.Preferences.Get("token", null), jobcard_detail);

            if (!result.success)
            {
                UserDialogs.Instance.Toast(result.error, new TimeSpan(0, 0, 0, 3));
                return;
            }

            UserDialogs.Instance.Toast("Jobacrd created successfully", new TimeSpan(0, 0, 0, 3));
            await page.Navigation.PopAsync();
        });
