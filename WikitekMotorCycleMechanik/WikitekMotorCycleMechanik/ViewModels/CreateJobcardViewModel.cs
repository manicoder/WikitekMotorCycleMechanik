        #region Methods
        public async void GetUserList()
        {
            //int index = 0;
            static_user_list = user_list = new ObservableCollection<MibudUser>();
            var result = await apiServices.GetUserList(Xamarin.Essentials.Preferences.Get("token", null));

            if (!result.status)
            {
                //DependencyService.Get<Interfaces.IToasts>().Show($"{ mode.status}");
                UserDialogs.Instance.Toast(result.message, new TimeSpan(0, 0, 0, 3));
                return;
            }

            if (result.results == null || (!result.results.Any()))
            {
                UserDialogs.Instance.Toast("User not found", new TimeSpan(0, 0, 0, 3));
                return;
            }

            static_user_list = user_list = new ObservableCollection<MibudUser>(result.results.ToList());
        }

        public async void GetVehicleList()
        {
            static_vehicle_list = vehicle_list = new ObservableCollection<VehicleListResult>();
            var result = await apiServices.GetVehicleList1(Xamarin.Essentials.Preferences.Get("token", null), selected_user.id);

            if (!result.status)
            {
                show_vehicle_view = false;
                UserDialogs.Instance.Toast(result.message, new TimeSpan(0, 0, 0, 3));
                return;
            }

            if (result.results == null || (!result.results.Any()))
            {
                show_vehicle_view = false;
                UserDialogs.Instance.Toast("User not found", new TimeSpan(0, 0, 0, 3));
                return;
            }

            static_vehicle_list = vehicle_list = new ObservableCollection<VehicleListResult>(result.results.ToList());
        }
